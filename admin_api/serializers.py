from rest_framework import serializers



class CreateAdminSerializer(serializers.Serializer):
    email = serializers.EmailField()
    phone_number = serializers.CharField()
    id_number = serializers.IntegerField()
    role = serializers.IntegerField()
    approver = serializers.EmailField()

class ApproveAdminSerializer(serializers.Serializer):
    email = serializers.EmailField()
    otp = serializers.IntegerField()

class SuspendAdminSerializer(serializers.Serializer):
    email = serializers.EmailField()

class ReactivateAdminSerializer(serializers.Serializer):
    email = serializers.EmailField()

class DeleteAdminSerializer(serializers.Serializer):
    email = serializers.EmailField()